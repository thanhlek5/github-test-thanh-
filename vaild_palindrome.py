# A phrase is a palindrome if, 
# after converting all uppercase letters into lowercase letters and removing all non-alphanumeric characters, 
# it reads the same forward and backward. 
# Alphanumeric characters include letters and numbers.
class Solution:
    def isPalindrome(self,s):
        s = str(s)
        clean_s = ''
        for i in s:
            if i.isalpha() or i.isdigit() : # có thể dùng if i.isalnum():
                clean_s += i.lower()        #isalnum() dùng để kiểm tra xem các ký tự trong chuỗi có phải là chữ hay số hay không 
        for j in range(0,int(len(clean_s)/2)):
            if clean_s[j] != clean_s[-j-1]:
                return False
        return True
